void RotacaoLL(ArvAVL *A){//*raiz = A, no = B
    printf("RotacaoLL\n");
    struct NO *B;//no aux
    B = (*A)->esq;//que ira receber a esquerda da raiz
    (*A)->esq = B->dir;//o filho da esquerda, recebe o filho da direita a esquerda
    B->dir = *A;//o filho da direita recebe raiz
    (*A)->altura = maior(altura_NO((*A)->esq),altura_NO((*A)->dir)) + 1;//alutura da riaz, passa a ser a maior altr entre o filho esq e dir + 1
    B->altura = maior(altura_NO(B->esq),(*A)->altura) + 1;//a altura do no passa a ser a altura da esq e da raiz mais 1;
    *A = B;
}

void RotacaoRR(ArvAVL *A){//RR
    printf("RotacaoRR\n");
    struct NO *B;
    B = (*A)->dir;//o no recebera, o no a dir da raiz
    (*A)->dir = B->esq;
    B->esq = (*A);
    (*A)->altura = maior(altura_NO((*A)->esq),altura_NO((*A)->dir)) + 1;
    B->altura = maior(altura_NO(B->dir),(*A)->altura) + 1;
    (*A) = B;
}

void RotacaoLR(ArvAVL *A){//LR
    RotacaoRR(&(*A)->esq);
    RotacaoLL(A);
}

void RotacaoRL(ArvAVL *A){//RL
    RotacaoLL(&(*A)->dir);
    RotacaoRR(A);
}

int insere_ArvAVL(ArvAVL *raiz, int valor){
    int res;
    if(*raiz == NULL){//árvore vazia ou nó folha
        struct NO *novo;
        novo = (struct NO*)malloc(sizeof(struct NO));
        if(novo == NULL)
            return 0;

        novo->info = valor;
        novo->altura = 0;
        novo->esq = NULL;
        novo->dir = NULL;
        *raiz = novo;
        return 1;
    }

    struct NO *atual = *raiz;
    if(valor < atual->info){//SE FOR MESNOR INSERIR NA ESQUERDA
        if((res = insere_ArvAVL(&(atual->esq), valor)) == 1){//INSERE, E SE DEU CERTO
            if(fatorBalanceamento_NO(atual) >= 2){//VERIFICA SE ESTA BALANCEADA
                if(valor < (*raiz)->esq->info ){//SE O VALOR EH MENOR, DO QUE O CONTEUDO DO FILHO DA ESQ DA RAIZ
                    RotacaoLL(raiz);//INSERIU A ESQ A ESQ
                }else{
                    RotacaoLR(raiz);//INSERIU A ESQ A DIR
                }
            }
        }
    }else{
        if(valor > atual->info){
            if((res = insere_ArvAVL(&(atual->dir), valor)) == 1){
                if(fatorBalanceamento_NO(atual) >= 2){
                    if((*raiz)->dir->info < valor){
                        RotacaoRR(raiz);//INSERIR A DIR A DIR
                    }else{
                        RotacaoRL(raiz);//INSERIR A DIR E A ESQ
                    }
                }
            }
        }else{
            printf("Valor duplicado!!\n");
            return 0;
        }
    }

    atual->altura = maior(altura_NO(atual->esq),altura_NO(atual->dir)) + 1;

    return res;
}

struct NO* procuraMenor(struct NO* atual){
    struct NO *no1 = atual;
    struct NO *no2 = atual->esq;
    while(no2 != NULL){
        no1 = no2;
        no2 = no2->esq;
    }
    return no1;
}

